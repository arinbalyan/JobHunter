const { Pool } = require('pg');
const pool = new Pool({ connectionString: process.env.DATABASE_URL });

const GIF = Buffer.from([
  0x47,0x49,0x46,0x38,0x39,0x61,0x01,0x00,0x01,0x00,
  0x80,0x00,0x00,0xff,0xff,0xff,0x00,0x00,0x00,
  0x21,0xf9,0x04,0x00,0x00,0x00,0x00,0x00,
  0x2c,0x00,0x00,0x00,0x00,0x01,0x00,0x01,0x00,
  0x00,0x02,0x02,0x44,0x01,0x00,0x3b,
]);

module.exports = async (req, res) => {
  const emailId = req.query.e;
  if (!emailId) return res.status(400).end('missing e');
  try {
    await pool.query('UPDATE tracking SET opened = true, opened_at = COALESCE(opened_at, now()) WHERE email_id = $1', [emailId]);
  } catch (_) {}
  res.setHeader('Content-Type', 'image/gif');
  res.setHeader('Cache-Control', 'no-store, max-age=0');
  res.status(200).end(GIF);
};
